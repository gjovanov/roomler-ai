// Tauri build script. Generates Tauri-runtime bindings + bundles
// front-end assets per `tauri.conf.json`. Required entry point — the
// macros in `main.rs` depend on the artifacts this produces.
//
// Also emits placeholder icon files (`icons/icon.ico`, `icons/icon.png`)
// at build time if absent. Tauri's Windows-resource generation insists
// on a valid `icon.ico` and the bundler wants the PNG; rather than
// commit binary blobs to the repo, generate minimal 1×1 placeholders
// here. The release-agent.yml CI pipeline overrides these with the
// real branded icons before invoking `cargo build --release` for the
// production wizard EXE artifact.
//
// Mirrors `agents/roomler-agent-tray/build.rs`; see that file for the
// full rationale on the placeholder-image bytes.

